"use strict";

import { paths } from "./conf.js";
import connect from "gulp-connect";
import gulp from "gulp";
import runSequence from "gulp4-run-sequence";

gulp.task("node_modules:fonts", async function () {
	return gulp
		.src(["node_modules/font-awesome/fonts/*.*"])
		.pipe(gulp.dest(paths.docs + "/fonts"))
		.pipe(connect.reload());
});

gulp.task("node_modules:images", async function () {
	runSequence("node_modules:images_twemoji");
});

gulp.task("node_modules:images_twemoji", function () {
	return gulp
		.src(["node_modules/twemoji/2/svg/*.*"])
		.pipe(gulp.dest(paths.docs + "/img/twemoji"))
		.pipe(connect.reload());
});

gulp.task("node_modules:styles", function () {
	return gulp
		.src(["node_modules/font-awesome/css/font-awesome.min.css"])
		.pipe(gulp.dest(paths.docs + "/css"))
		.pipe(connect.reload());
});

gulp.task("node_modules", async function () {
	runSequence("node_modules:fonts", "node_modules:images", "node_modules:styles");
});
